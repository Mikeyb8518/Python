# Program that calculates the distance traveled  by a car for 6 hours,
# 10 hours,  and 15 hours, given the car's speed in miles per hour

# MPH variable set to 70
MPH = 70

time = 6
distance  = time * MPH
print('\nThe distance the car travels in', time, 'hours is', distance, 'miles')

time  = 10
distance   = time * MPH
print('The distance  the car travels in', time, 'hours is', distance, 'miles')

time   = 15
distance   = time * MPH
print('The distance  the car travels in', time, 'hours is', distance, 'miles')
